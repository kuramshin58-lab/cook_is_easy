// Function to escape JSON for CSV
function escapeForCSV(obj: any): string {
  const jsonString = JSON.stringify(obj);
  return jsonString.replace(/"/g, '""');
}

// Generate CSV
function generateCSV(): string {
  const header = 'title,description,difficulty,prep_time,cook_time,servings,ingredients,instructions,calories,protein,fats,carbs,tags,source_url';

  const rows = recipes.map(recipe => {
    const ingredients = escapeForCSV(recipe.ingredients);
    const instructions = escapeForCSV(recipe.instructions);

    return `"${recipe.title}","${recipe.description}","${recipe.difficulty}",${recipe.prep_time},${recipe.cook_time},${recipe.servings},"${ingredients}","${instructions}",${recipe.calories},${recipe.protein},${recipe.fats},${recipe.carbs},"${recipe.tags}","${recipe.source_url}"`;
  });

  return [header, ...rows].join('\n');
}

